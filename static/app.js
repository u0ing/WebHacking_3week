// 폼 제출(submit) 시 실행되는 함수
// 글쓰기/수정 폼 둘 다 이 함수를 공통으로 사용함
function checkForm() {
  // id="title"인 입력칸(제목)의 현재 값을 가져옴
  let title = document.getElementById("title").value;

  // trim() : 앞뒤 공백을 제거해줌 (스페이스바만 입력한 경우도 걸러내기 위함)
  if (title.trim() === "") {
    // 제목이 비어있으면 경고창을 띄움
    alert("제목을 입력해주세요!");

    // false를 반환하면 폼이 실제로 제출되지 않고 멈춤
    return false;
  }

  // 제목이 잘 입력되어 있으면 정상적으로 폼 제출을 진행함
  return true;
}
